package forLoop;

public class forLoop {
    public static void main(String[] args) {
        System.out.println("For loop");
        for(int count = 1; count <= 10 ; count++ ){
            System.out.println(count);
        }
        System.out.println();
        System.out.println("While Count: ");
        int  i = 1;
        while (i <= 10){
            System.out.println(i + " ");
            i++;
        }
    }
}
